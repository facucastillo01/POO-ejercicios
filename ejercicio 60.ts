let numeroo = Number(prompt("Ingrese un número del 1 al 5:"));

if (numeroo === 1) console.log("uno");
else if (numeroo === 2) console.log("dos");
else if (numeroo === 3) console.log("tres");
else if (numeroo === 4) console.log("cuatro");
else if (numeroo === 5) console.log("cinco");
else console.log("Inválido");